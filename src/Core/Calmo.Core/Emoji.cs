    {
        public static bool ContainsEmoji(this string value, params string[] emoji)
        {
            if (String.IsNullOrEmpty(value) || emoji.Length == 0) return false;

            var regex = new Regex(String.Join(@"|", emoji));
            return regex.IsMatch(value);
